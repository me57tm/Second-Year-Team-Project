package application;

public class connecting {
	public double rotation, x, y;

	public void rotating(double rotation) {
		this.rotation = rotation;
	}

	public void xing(double x) {
		this.x = x;
	}

	public void ying(double y) {
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}
	public double getRotation() {
		return rotation;
	}
	
}
